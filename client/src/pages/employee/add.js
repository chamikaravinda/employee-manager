import Head from 'next/head';
import styles from '@/styles/Employee.addEdit.module.css'
import { Link, Grid, Snackbar, Button, Alert } from '@mui/material';
import EmployeeForm from '@/components/employee.form';
import { useState } from 'react';
import axios from 'axios';


export default function AddEmployee() {

  const [state, setState] = useState({
    isOpenNotification: false,
    message: "",
    severity: "info",
  });

  const { message, severity, isOpenNotification } = state;

  function addEmployee(employee){
    axios.post(`${process.env.NEXT_PUBLIC_API_URL}/employees`, employee)
    .then(function (response) {
      setState({
        isOpenNotification: true,
        message: "Add employee success",
        severity: "success",
      });
    })
    .catch(function (error) {
      console.log(error);
      setState({
        isOpenNotification: true,
        message: "Error in adding the employee",
        severity: "error",
      });
    });
  }

  const handleClose = () => {
    setState({
      isOpenNotification: false,
      message: '',
      severity: "info",
    });  
  };

  return (
    <>
      <Head>
        <title>Employee Manager | Add</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="main">
        <Grid container spacing={2}>
          <Grid lg={10}>
          </Grid>
          <Grid lg={2}>
            <Link href="/employee/list">
                <Button variant="contained" className={styles.btnAddEmployee}>List View</Button>
            </Link>
          </Grid>
        </Grid>
        <Grid container columns={12} >
          <Grid lg={4} md={3}>
          </Grid>
          <Grid lg={4} md={6} className={styles.form}>
            <EmployeeForm action={"Add"} actionFunction={addEmployee}/>
          </Grid>
        </Grid>
      </main>
      <Snackbar 
        open={isOpenNotification} 
        anchorOrigin={{
          vertical: 'top',
          horizontal: 'left',
        }}
        autoHideDuration={6000} 
        onClose={handleClose}
      >
        <Alert onClose={handleClose} severity={severity} sx={{ width: '100%' }}>
          {message}
        </Alert>
      </Snackbar>
    </>
  )
}